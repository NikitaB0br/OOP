package org.example;

public class ItemsStatistic implements Printer {
    public double price;
    public double amount;
    public int id;

    public ItemsStatistic(int id, double amount, double price ) {
        this.id = id;
        this.amount = amount;
        this.price = price;
    }

    public void printInfo() {
        this.printInfo();
    }
}
